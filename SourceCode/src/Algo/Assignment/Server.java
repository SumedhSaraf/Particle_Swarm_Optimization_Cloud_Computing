package Algo.Assignment;

public class Server {

	
	int serverX;
	int serverY;
	int serverOffering;
	int serverID;
	
	
	public int getServerID() {
		return serverID;
	}
	public void setServerID(int serverID) {
		this.serverID = serverID;
	}
	public int getServerX() {
		return serverX;
	}
	public void setServerX(int serverX) {
		this.serverX = serverX;
	}
	public int getServerY() {
		return serverY;
	}
	public void setServerY(int serverY) {
		this.serverY = serverY;
	}
	public int getServerOffering() {
		return serverOffering;
	}
	public void setServerOffering(int serverOffering) {
		this.serverOffering = serverOffering;
	}
	
	
}
