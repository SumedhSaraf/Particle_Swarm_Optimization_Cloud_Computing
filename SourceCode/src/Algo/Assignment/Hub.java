package Algo.Assignment;

public class Hub {


private int hubX;
private int hubY;
private int packetConstraint;
private int requirement;

public int getHubX() {
	return hubX;
}
public void setHubX(int hubX) {
	this.hubX = hubX;
}
public int getHubY() {
	return hubY;
}
public void setHubY(int hubY) {
	this.hubY = hubY;
}
public int getPacketConstraint() {
	return packetConstraint;
}
public void setPacketConstraint(int packetConstraint) {
	this.packetConstraint = packetConstraint;
}
public int getRequirement() {
	return requirement;
}
public void setRequirement(int requirement) {
	this.requirement = requirement;
}




}
